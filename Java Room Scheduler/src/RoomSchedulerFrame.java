    private void facultyStatusSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_facultyStatusSubmitButtonActionPerformed
        Object faculty = facultyStatusComboBox.getSelectedItem();
        ArrayList<ArrayList<String>> facultyStatusList = Faculty.getFacultyStatus(faculty);
        Collections.reverse(facultyStatusList);
        DefaultTableModel model = (DefaultTableModel) facultyStatusTable.getModel();
        Object [] row = new Object[2];
        for (int b = model.getRowCount() - 1; b >= 0; b--){
            model.removeRow(b);
        }
        for (int l = 0; l < facultyStatusList.size(); l++){
            row[0] = facultyStatusList.get(l).get(0);
            row[1] = facultyStatusList.get(l).get(1);
            model.addRow(row);
        }
    }//GEN-LAST:event_facultyStatusSubmitButtonActionPerformed

