    private void getReservationsByDateSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getReservationsByDateSubmitButtonActionPerformed
        ArrayList<ArrayList<String>> reservationsByDateList = Reservations.getReservationsByDate(dateStatusComboBox.getSelectedItem());
        Collections.reverse(reservationsByDateList);
        DefaultTableModel model = (DefaultTableModel) StatusTable.getModel();
        Object[] row = new Object[2];
        for (int y = model.getRowCount() - 1; y >= 0; y--){
            model.removeRow(y);
        }
        for (int n=0;n<reservationsByDateList.size();n++){
            row[0] = reservationsByDateList.get(n).get(0);
            row[1] = reservationsByDateList.get(n).get(1);
            model.addRow(row);
        }

    }//GEN-LAST:event_getReservationsByDateSubmitButtonActionPerformed

    private void WaitlistSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WaitlistSubmitButtonActionPerformed
        ArrayList<ArrayList<String>> waitlistList = Waitlist.getWaitlistByDate();
        Collections.reverse(waitlistList);
        DefaultTableModel model = (DefaultTableModel) WaitlistTable.getModel();
        Object[] row = new Object[3];
        for (int z = model.getRowCount() - 1; z >= 0; z--){
            model.removeRow(z);
        }
        for (int w = 0; w<waitlistList.size(); w++){
            row[0] = waitlistList.get(w).get(0);
            row[1] = waitlistList.get(w).get(1);
            row[2] = waitlistList.get(w).get(2);
            model.addRow(row);
        }
    }//GEN-LAST:event_WaitlistSubmitButtonActionPerformed

